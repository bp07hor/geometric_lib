def area(a, b):
    '''Принимает числа a и b, являющиеся сторонами прямоугольника, возвращает произведение a на b, что является площадью прямоугольника'''
    return a * b

def perimeter(a, b):
    '''Принимает числа a и b, являющиеся сторонами прямоугольника, возвращает произведение 2 на сумму a и b, что является периметром прямоугольника'''
    return 2 * (a + b)