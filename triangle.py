def area(a, h):
    '''Принимает a и h, являющиеся стороной и высотой треугольника соответственно, возвращает произведени a на h поделенное на 2, что является площадью треугольника'''
    return a * h / 2

def perimeter(a, b, c):
    '''Принимает a, b и c, являющиеся сторонами треугольника, возвращает сумму a, b и c, что является периметром треугольника'''
    return a + b + c